    public function deleted(User $user)
    {
        /* Unlink old image from helper function call */
        !empty($user->photo) ? UnlinkImage($user->photo) : '';

        foreach($user->drivers as $driver){
            /* Unlink old image from helper function call */
            !empty($driver->photo) ? UnlinkImage($driver->photo) : '';
            $driver->delete();
        }

        $user->drivers()->detach();

    }

